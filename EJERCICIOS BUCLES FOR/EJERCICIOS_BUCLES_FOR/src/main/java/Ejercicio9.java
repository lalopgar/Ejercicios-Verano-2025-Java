//Imprimir triángulo rectángulo de asteriscos

public class Ejercicio9 {
    public static void main(String[] args) {

        for (int i=1;i<=5;i++){ //filas
            for (int j=1;j<=i;j++){  //columnas

                System.out.print("*");
            }
            System.out.println();
        }
    }
}








